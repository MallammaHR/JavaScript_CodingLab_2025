//post-incremnt:++
let a=2;
let b=a++;
console.log(a);//3
console.log(b);//2

let m=-999;
let n=m++;
console.log(m);//-998
console.log(n);//-999

//pree-increment
let p=2;
let q=++p;
console.log(p);//3
console.log(q); //3

//post-decrement
let k=-89;
let h=k--;
console.log(k);//-90
console.log(h);//-89

//pre-decrement
let v=2;
let z=--v;
console.log(v);//1
console.log(z);//1

let total=100;
console.log(total++);//101